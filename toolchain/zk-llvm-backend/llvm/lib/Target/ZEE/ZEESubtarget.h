#ifndef LLVM_LIB_TARGET_ZEE_ZEESUBTARGET_H
#define LLVM_LIB_TARGET_ZEE_ZEESUBTARGET_H

#include "ZEEFrameLowering.h"
#include "ZEEInstrInfo.h"
#include "ZEEISelLowering.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "ZEEGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class ZEESubtarget : public ZEEGenSubtargetInfo {
  virtual void anchor();
  bool ExtendedInsts;
  ZEEFrameLowering FrameLowering;
  ZEEInstrInfo InstrInfo;
  ZEERegisterInfo RegInfo;
  ZEETargetLowering TLInfo;

public:
  // Initializes the data members to match that of the specified triple.
  ZEESubtarget(const Triple &TT, StringRef CPU, StringRef FS,
                 StringRef ABIName, const TargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const ZEEFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const ZEEInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const ZEERegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }
  const ZEETargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
};
} // End llvm namespace

#endif
